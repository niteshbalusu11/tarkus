#!/usr/bin/env node
// Reads the design stages from ./curriculum-skill/stages/ and emits
// convex/curriculumSkill.ts so the prep actions can inject them into
// OpenRouter system prompts. Re-run after editing the skill.

import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const stagesDir = join(root, 'curriculum-skill', 'stages')
const outputPath = join(root, 'convex', 'curriculumSkill.ts')

const stages = ['frame', 'story', 'cases', 'design']

const sections = stages.map((stage) => {
  const path = join(stagesDir, `${stage}.md`)
  if (!existsSync(path)) {
    throw new Error(`Missing curriculum-skill stage file: ${path}`)
  }
  return { stage, body: readFileSync(path, 'utf8').trimEnd() }
})

function escape(value) {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
}

const banner = `// AUTO-GENERATED by scripts/sync-curriculum-skill.mjs.
// Source: curriculum-skill/stages/*.md.
// Run \`bun --bun run sync:curriculum-skill\` to refresh.
`

const constants = sections
  .map(
    ({ stage, body }) =>
      `export const ${stage.toUpperCase()}_STAGE = \`${escape(body)}\`\n`,
  )
  .join('\n')

const combined = `\nexport const CURRICULUM_SKILL_STAGES = [
  { stage: 'frame', body: FRAME_STAGE },
  { stage: 'story', body: STORY_STAGE },
  { stage: 'cases', body: CASES_STAGE },
  { stage: 'design', body: DESIGN_STAGE },
] as const\n
export const CURRICULUM_SKILL_PROMPT = CURRICULUM_SKILL_STAGES.map(
  ({ stage, body }) => \`<curriculum_skill_stage name="\${stage}">\\n\${body}\\n</curriculum_skill_stage>\`,
).join('\\n\\n')\n`

writeFileSync(outputPath, banner + '\n' + constants + combined, 'utf8')
console.log(
  `Wrote ${outputPath} from ${sections.length} stage files (${sections.reduce((sum, section) => sum + section.body.length, 0)} chars).`,
)
